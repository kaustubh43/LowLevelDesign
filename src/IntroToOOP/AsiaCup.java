package IntroToOOP;

public class AsiaCup {
    private String name;

    public AsiaCup(String name) {
        this.name = name;
    }

    public AsiaCup(){
        this.name = "Common Asia Cup";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



}